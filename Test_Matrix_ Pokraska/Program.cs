void SeeArray(int[,] pic)
{
    for (int i = 0; i < pic.GetLength(0); i++)
    {
        for (int j = 0; j < pic.GetLength(1); j++)
        {
            if (pic[i, j] == 0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}

void FillImage(int[,] pic)
{
    int check1J = 0;
    int check2J = 0;

    for (int i = 0; i < pic.GetLength(0); i++)
    {
        for (int j = 0; j < pic.GetLength(1) - 1; j++)
        {
            if (pic[i, j] == 1 && pic[i, j + 1] == 0)
            {
                check1J = j + 1;
                while (j < pic.GetLength(1) - 1)
                {
                    if (pic[i, j] == 0 && pic[i, j + 1] == 1)
                    {
                        check2J = j;
                    }
                    j++;
                }

            }
        }
        if (check1J < check2J)
        {
            while (check1J <= check2J)
            {
                pic[i, check1J] = 1;
                check1J++;
            }
        }
    }
}

